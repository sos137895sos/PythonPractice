things = ['mozzarella', 'cinderella', 'salmonella']
for i in range(len(things)) :
    if things[i] == 'cinderella' :
        things[i] = things[i].capitalize()
        print(things)
for m in range(len(things)) :
    if things[m] == 'mozzarella' :
        things[m] = things[m].upper()
        print(things)
        